function isChineseChar(char: string): boolean {
  return /[\u4e00-\u9fa5]/.test(char)
}

function segmentText(text: string): string[] {
  const words: string[] = []
  
  const cleanText = text.trim()
  if (!cleanText) return words

  const chinesePattern = /[\u4e00-\u9fa5]+/g
  const englishPattern = /[a-zA-Z]+/g
  const numberPattern = /\d+/g

  const chineseMatches = cleanText.match(chinesePattern) || []
  const englishMatches = cleanText.match(englishPattern) || []
  const numberMatches = cleanText.match(numberPattern) || []

  englishMatches.forEach(word => {
    if (word.length >= 2) {
      words.push(word.toLowerCase())
    }
  })

  numberMatches.forEach(num => {
    if (num.length >= 2) {
      words.push(num)
    }
  })

  chineseMatches.forEach(chunk => {
    if (chunk.length <= 1) return

    for (let len = 4; len >= 2; len--) {
      for (let i = 0; i <= chunk.length - len; i++) {
        const word = chunk.slice(i, i + len)
        if (!STOP_WORDS.has(word) && word.length >= 2) {
          words.push(word)
        }
      }
    }

    for (let i = 0; i < chunk.length; i++) {
      const char = chunk[i]
      if (!STOP_WORDS.has(char) && isChineseChar(char)) {
        words.push(char)
      }
    }
  })

  return words
}

function extractPhrases(text: string, n: number = 2): string[] {
  const phrases: string[] = []
  const cleanText = text.trim()
  if (!cleanText) return phrases

  const chinesePattern = /[\u4e00-\u9fa5]+/g
  const chineseMatches = cleanText.match(chinesePattern) || []

  chineseMatches.forEach(chunk => {
    if (chunk.length < n) return

    for (let i = 0; i <= chunk.length - n; i++) {
      const phrase = chunk.slice(i, i + n)
      let hasStopWord = false
      for (let j = 0; j < phrase.length; j++) {
        if (STOP_WORDS.has(phrase[j])) {
          hasStopWord = true
          break
        }
      }
      if (!hasStopWord) {
        phrases.push(phrase)
      }
    }
  })

  return phrases
}

function calculateWordFrequency(words: string[], topN: number = 50): WordFrequency[] {
  const freqMap = new Map<string, number>()

  words.forEach(word => {
    if (word.length < 1) return
    freqMap.set(word, (freqMap.get(word) || 0) + 1)
  })

  const sorted = Array.from(freqMap.entries())
    .map(([word, count]) => ({ word, count, weight: 0 }))
    .sort((a, b) => b.count - a.count)

  const maxCount = sorted.length > 0 ? sorted[0].count : 1

  const result = sorted
    .filter(item => item.count >= 2 || item.word.length >= 3)
    .slice(0, topN)
    .map(item => ({
      ...item,
      weight: Math.round((item.count / maxCount) * 100)
    }))

  return result
}

function calculatePhraseFrequency(texts: string[], topN: number = 20): PhraseFrequency[] {
  const phraseMap = new Map<string, number>()

  texts.forEach(text => {
    const phrases2 = extractPhrases(text, 2)
    const phrases3 = extractPhrases(text, 3)
    const phrases4 = extractPhrases(text, 4)
    const allPhrases = [...phrases4, ...phrases3, ...phrases2]

    allPhrases.forEach(phrase => {
      phraseMap.set(phrase, (phraseMap.get(phrase) || 0) + 1)
    })
  })

  const sorted = Array.from(phraseMap.entries())
    .map(([phrase, count]) => ({ phrase, count }))
    .sort((a, b) => b.count - a.count)

  const result: PhraseFrequency[] = []
  const seen = new Set<string>()

  for (const item of sorted) {
    if (result.length >= topN) break

    let isSubPhrase = false
    for (const existing of result) {
      if (existing.phrase.includes(item.phrase) && existing.count >= item.count) {
        isSubPhrase = true
        break
      }
    }

    if (!isSubPhrase && item.count >= 2) {
      const key = item.phrase
      if (!seen.has(key)) {
        seen.add(key)
        result.push(item)
      }
    }
  }

  return result
}

function calculateAnswerHeat(texts: string[], wordFrequencies: WordFrequency[]): AnswerHeat[] {
  const highFreqWords = new Set(wordFrequencies.slice(0, 20).map(w => w.word))

  return texts.map((text, index) => {
    const wordCount = text.length
    const cleanText = text.trim()
    
    let keywordCount = 0
    highFreqWords.forEach(word => {
      if (cleanText.includes(word)) {
        keywordCount++
      }
    })

    const lengthScore = Math.min(wordCount / 50, 1) * 40
    const keywordScore = highFreqWords.size > 0 ? (keywordCount / highFreqWords.size) * 40 : 0
    const sentenceCount = text.split(/[。！？.!?]+/).filter(s => s.trim()).length
    const structureScore = Math.min(sentenceCount / 3, 1) * 20

    const heat = Math.round(lengthScore + keywordScore + structureScore)

    return {
      index,
      content: text,
      heat: Math.min(heat, 100),
      wordCount
    }
  }).sort((a, b) => b.heat - a.heat)
}

export function analyzeWordCloud(textAnswers: string[]): WordCloudData {
  if (!textAnswers || textAnswers.length === 0) {
    return {
      highFrequencyWords: [],
      corePhrases: [],
      answerHeats: [],
      totalAnswers: 0,
      avgWordCount: 0
    }
  }

  const validAnswers = textAnswers.filter(t => t && t.trim().length > 0)
  const allWords: string[] = []

  validAnswers.forEach(text => {
    const words = segmentText(text)
    allWords.push(...words)
  })

  const highFrequencyWords = calculateWordFrequency(allWords, 30)
  const corePhrases = calculatePhraseFrequency(validAnswers, 15)
  const answerHeats = calculateAnswerHeat(validAnswers, highFrequencyWords)
  const totalWordCount = validAnswers.reduce((sum, t) => sum + t.length, 0)

  return {
    highFrequencyWords,
    corePhrases,
    answerHeats,
    totalAnswers: validAnswers.length,
    avgWordCount: Math.round(totalWordCount / validAnswers.length)
  }
}
